SELECT 
    U.ID AS idUsuario,
    U.NOME AS nomeUsuario,
    U.CARGO AS cargo,
    S.ID AS idSala,
    S.NUMERO AS numeroSala,
    SP.DATAINICIO AS dataInicio,
    SP.DATAFIM AS dataFim,
    A.DESCRICAO AS atividade,
    O.DESCRICAO AS ocorrencia
FROM
    USUARIO U,
    SALA S,
    SERVICOPRESTADO SP,
    ATIVIDADE A,
    OCORRENCIA O
WHERE
    SP.IDUSUARIO = U.ID
        AND S.ID = SP.IDSALA;