CREATE TABLE USUARIO
(
    ID            INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    NOME          VARCHAR(255),
    CPF           VARCHAR(11),
    TELEFONE      VARCHAR(11),
    DATANASCIMENTO  DATE,
    CTPS          VARCHAR(11) UNIQUE,
    NIVELACESSO VARCHAR(255),
    CARGO VARCHAR(255),
	MATRICULA 	  NUMERIC(6) UNIQUE,
    SENHA 		  VARCHAR(55),
    STATUSUSUARIO VARCHAR(255),
    IDULTIMOAFASTAMENTO INT,
    DATACONTRATACAO DATE,
    DATADESLIGAMENTO DATE
);

CREATE TABLE AFASTAMENTO (
    ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    NATUREZA VARCHAR(255),
    DESCRICAO VARCHAR(255),
    DATAINICIO DATE,
    DATAFIM DATE,
    IDUSUARIO INT,
    FOREIGN KEY (IDUSUARIO)
        REFERENCES USUARIO (ID)
);

CREATE TABLE ATIVIDADE
(
    ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    DESCRICAO     VARCHAR(255),
    CARGO   VARCHAR(255)
);

CREATE TABLE SALA
(
    ID         INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    NUMERO     VARCHAR(10),
    DISPONIVEL BOOLEAN
);

CREATE TABLE SERVICOPRESTADO
(
    ID            INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    IDSALA        INT,
    FOREIGN KEY (IDSALA) REFERENCES SALA (ID),
    DATAINICIO    DATETIME,
    DATAFIM       DATETIME,
    IDUSUARIO INT,
    FOREIGN KEY (IDUSUARIO) REFERENCES USUARIO (ID),
    IDATIVIDADE INT,
    FOREIGN KEY (IDATIVIDADE) REFERENCES ATIVIDADE (ID),
    IDOCORRENCIA INT
);

CREATE TABLE OCORRENCIA (
    ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    DESCRICAO VARCHAR(255),
    CATEGORIA VARCHAR (255),
    STATUSOCORRENCIA BOOLEAN,
    DATAOCORRENCIA    DATETIME,
    IDSERVICOPRESTADO INT,
    FOREIGN KEY (IDSERVICOPRESTADO)
        REFERENCES SERVICOPRESTADO (ID)
);

